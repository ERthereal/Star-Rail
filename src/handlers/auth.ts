import { GetPlayerTokenReq } from "../../resources/autogenerated/GetPlayerTokenReq";
import { GetPlayerTokenRsp } from "../../resources/autogenerated/GetPlayerTokenRsp";
import { PacketContext, RouteManager } from "../network/route";

export class AuthHandler{
    constructor(routeManager: RouteManager){
        routeManager.on(GetPlayerTokenReq, this.getPlayerTokenReq);
    }

    public getPlayerTokenReq(context: PacketContext<GetPlayerTokenReq>){
        const rsp = GetPlayerTokenRsp.create();
        const seed = BigInt(0);
        rsp.uid = 12312310; //hardcoded for now. TODO: database
        rsp.accountType = context.request.accountType!;
        rsp.accountUid = context.request.accountUid!;
        rsp.token = context.request.accountToken!;
        rsp.secretKey = seed.toString();
        context.send(GetPlayerTokenRsp, rsp);
    }
}